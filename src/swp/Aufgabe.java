package swp;

public class Aufgabe {
	
	private String id;
	
	private String bez;
	private String link;
	private String bild_link;
	
	public Aufgabe () {
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getBez() {
		return bez;
	}

	public void setBez(String bez) {
		this.bez = bez;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getBild_link() {
		return bild_link;
	}

	public void setBild_link(String bild_link) {
		this.bild_link = bild_link;
	}

}
